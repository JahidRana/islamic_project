<h3>মদিনায় হিজরত </h3>

<p>৬২২ খ্রিষ্টাব্দে মুসলিমরা মদিনায় হিজরত করতে শুরু করে। কয়েকটি দলে এই হিজরত হয়। মুহাম্মদ ও আবু বকর একই সাথে হিজরত করেন। মুহাম্মদ তার কাছে গচ্ছিত মক্কাবাসীর সম্পদ ফিরিয়ে দেওয়ার জন্য আলি ইবনে আবি তালিবকে দায়িত্ব দিয়ে যান। হামলার আশঙ্কায় তারা মদিনামুখী উত্তরের পথ না ধরে দক্ষিণমুখী ইয়েমেনগামী পথ ধরে অগ্রসর হন এবং পাঁচ মাইল দূরে সাওর পর্বতের গুহায় আশ্রয় নেন[১৪]। আবু বকরের ছেলে আবদুল্লাহ ইবনে আবি বকর এসময় তাদের সাথে রাতে থাকতেন এবং ভোরের পূর্বে মক্কায় ফিরে আসতেন যাতে অন্যদের মনে সন্দেহ সৃষ্টি না হয়[১৪]। এছাড়াও আবু বকরের এক দাস আমির বিন ফুহাইরাহ পর্বত পর্যন্ত ছাগল চরাতেন যাতে আবদুল্লাহ ইবনে আবু বকরের চলাচলের চিহ্ন মুছে যায়। এছাড়াও তিনি তাদের ছাগলের দুধ পান করাতেন[১৪]। আবু বকরের মেয়ে আসমা বিনতে আবি বকর তাদের খাবার নেয়ার কাজ করতেন।[১৫]

হিজরতের সংবাদ জানতে পেরে তাদের ধরার জন্য মক্কা থেকে বিভিন্ন লোক বিভিন্ন দিকে পাঠানো হয়। অণুসন্ধানকারীরা গুহা পর্যন্ত পৌছে গেলে আবু বকর চিন্তিত হয়ে উঠেন। মুহাম্মদ তাকে অভয় দিয়ে বলেন,

এরূপ দুজন সম্পর্কে তোমার কী ধারণা যাদের তৃতীয়জন হলেন আল্লাহ।[১৪]

এ বিষয়ে কুরআনে উল্লেখ রয়েছে,

যদি তোমরা তাকে সাহায্য না কর, (তবে স্মরণ কর) আল্লাহ তাকে সাহায্য করেছিলেন যখন অবিশ্বাসীরা তাকে তাড়িয়ে দিয়েছিল। সে ছিল দুজনের একজন। যখন তারা গুহার মধ্যে ছিল, সে তখন তার সঙ্গীকে বলেছিল, "মন খারাপ করো না, আল্লাহ তো আমাদের সাথেই আছেন"। তারপর আল্লাহ তার উপর প্রশান্তি বর্ষণ করলেন। (সূরা তওবা, আয়াত ৪০)

এই আয়াতে দুজনের একজন দ্বারা আবু বকরকে বোঝানো হয়েছে।

গুহায় তিনদিন ও তিনরাত অতীবাহিত করার পর তারা দুজন মদিনার দিকে অগ্রসর হন। তারা মদিনার শহরতলী কুবায় পৌঁছে সেখানে কিছু সময় অতীবাহিত করেন। এরপর তারা মদিনায় পৌঁছান।</p>


<h3>কুরআন সংকলন</h3>
<p>আবু বকর সর্বপ্রথম গ্রন্থাকারে কুরআন সংকলন করেন। ইতিপূর্বে কুরআনের বিভিন্ন অংশ বিচ্ছিন্নভাবে লিপিবদ্ধ ছিল। এছাড়াও হাফেজরা কুরআন মুখস্থ করে রাখতেন। ইয়ামামার যুদ্ধে অনেক হাফেজ শহিদ হলে উমর ইবনুল খাত্তাব গ্রন্থাকারে কুরআন সংকলনের জন্য আবেদন জানান। এই প্রথা মুহাম্মদের সময় ছিল না বলে প্রথমে আবু বকর এতে রাজি ছিলেন না। কিন্তু উমর তাকে রাজি করাতে সক্ষম হন। এজন্য জায়েদ ইবনে সাবিতকে প্রধান করে একটি কমিটি গঠন করা হয়। এর সদস্যরা সবাই হাফেজ ছিলেন। তারা কুরআনের সকল অংশ সংগ্রহ করে একক গ্রন্থ হিসেবে লিপিবদ্ধ করেন এবং হাফেজদের স্মৃতির সাথে সেগুলো মিলিয়ে দেখা হয়।

মৃত্যুর আগে আবু বকর কুরআনের এই কপিটি তার উত্তরসূরি উমর ইবনুল খাত্তাবকে দিয়ে যান। উমরের শাসনকালে এটি তার কাছেই রক্ষিত ছিল। উমর কুরআনটি তার মেয়ে ও মুহাম্মদের স্ত্রী হাফসা বিনতে উমরকে দিয়ে যান। পরবর্তী খলিফা উসমান ইবনে আফফান এই কুরআনের আরো প্রতিলিপি তৈরি করিয়ে তা খিলাফতের বিভিন্ন স্থানে প্রেরণ করেছিলেন।</p>


<h3>মৃত্যু </h3>
<p>৬৩৪ সালের ২৩ আগস্ট অসুস্থ হয়ে পড়েন। অসুস্থতার মাত্রা বৃদ্ধি পাওয়ায় তিনি বিছানায় শায়িতাবস্থায় থাকেন। আবু বকর তার উত্তরসূরি মনোনীত করার জন্য প্রয়োজনীয়তা অণুভব করেন যাতে তার মৃত্যুর পর মুসলিমদের মধ্যে সমস্যা দেখা না দেয়। অন্যান্য সাহাবীদের সাথে পরামর্শ করে তিনি উমর ইবনুল খাত্তাবকে তার উত্তরসূরি হিসেবে নিয়োগ দেন।

৬৩৪ সালের ২৩ আগস্ট আবু বকর মারা যান। আয়িশার ঘরে মুহাম্মদের পাশে তাকে দাফন করা হয়।</p>
