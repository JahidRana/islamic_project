 <h3>আবু বকর </h3>
 <p>আবু বকর (আরবি: أبو بكر‎‎) (২৭ অক্টোবর ৫৭৩ খ্রিষ্টাব্দ – ২৩ আগস্ট ৬৩৪ খ্রিষ্টাব্দ) ছিলেন মুহাম্মাদ (সাঃ) এর একজন প্রধান সাহাবি, ইসলামের প্রথম খলিফা এবং প্রথম মুসলিমদের মধ্যে অন্যতম। প্রাপ্তবয়স্কদের মধ্যে প্রথম ইসলাম গ্রহণের সম্মান তাকে দেওয়া হয়। এছাড়া তিনি রাসুল মুহাম্মাদ (সাঃ) এর শ্বশুর ছিলেন।[১] রাসুল মুহাম্মাদ (সাঃ) এর মৃত্যুর পর তিনি খলিফা হন এবং মুসলিমদের নেতৃত্ব দেন। মুহাম্মাদ (সাঃ) এর প্রতি অতুলনীয় বিশ্বাসের জন্য তাকে “সিদ্দিক” বা বিশ্বস্ত উপাধি প্রদান করা হয়েছে।[২] তবে এ ব্যাপারে কোন হাদিস পাওয়া যায়নি। হাদিসে অন্য তিনজনকে সিদ্দীক উপাধীকরণ করা হয়েছে। মিরাজের ঘটনা এক ব্যক্তির নিকট শুনে বিশ্বাস করেছিলেন। তাই তাকে আবু বকর সিদ্দিক নামেও সম্বোধন করা হয়।

তরুণ বয়সে আবু বকর(রাঃ)একজন বণিক হিসেবে জীবিকা নির্বাহ শুরু করেন। তিনি প্রতিবেশী সিরিয়া, ইয়েমেন ও অন্যান্য অঞ্চলে ব্যবসার কারণে ভ্রমণ করেছেন। এর মাধ্যমে তিনি সম্পদশালী হয়ে উঠেন এবং অভিজ্ঞতা অর্জন করেন। তিনি তার গোত্রের একজন নেতা হয়ে উঠেছিলেন।[৩] ইয়েমেন থেকে বাণিজ্য শেষে ফেরার পর তিনি মুহাম্মাদ (সাঃ) এর ইসলাম প্রচারের সংবাদ পান। এরপর তিনি ইসলাম গ্রহণ করেন। তার ইসলাম গ্রহণ অন্য অনেকের ইসলাম গ্রহণের উৎসাহ যুগিয়েছে।[৪] আবু বকরের(রাঃ) মেয়ে আয়িশার সাথে মুহাম্মাদ (সাঃ) এর বিয়ের ফলে তাদের দুজনের সম্পর্ক আরো ঘনিষ্ঠ হয়ে উঠে।[২]

আবু বকর একজন একনিষ্ঠ সহচর হিসেবে মুহাম্মাদ (সাঃ) এর সহযোগিতা করেছেন।[৫] তার জীবদ্দশায় আবু বকর বেশ কয়েকটি যুদ্ধে অংশ নিয়েছিলেন। তাবুকের যুদ্ধে তিনি তার সমস্ত সম্পদ দান করে দেন।[৬] হুদায়বিয়ার সন্ধিতে আবু বকর অংশ নিয়েছিলেন এবং তিনি ছিলেন এই চুক্তির অন্যতম সাক্ষী।[৬]

আবু বকরের খিলাফত দুই বছরের কিছু বেশি সময় স্থায়ী হয়েছিল। এরপর তিনি অসুস্থ হয়ে মারা যান। তার খিলাফতকাল দীর্ঘ না হলেও তিনি একজন সফল শাসক ছিলেন। মুহাম্মাদ (সাঃ) এর মৃত্যুর পর নবী দাবি করা ব্যক্তিদের তিনি রিদ্দার যুদ্ধে সফলভাবে দমন করেছেন এবং তৎকালীন দুটি পরাশক্তি পারস্য ও বাইজেন্টাইনদের উপর সফল অভিযান পরিচালনা করেছেন। অভিযানের ধারাবাহিকতায় কয়েক দশকে মুসলিম খিলাফত ইতিহাসের সর্ববৃহৎ সাম্রাজ্যের একটিতে পরিণত হয়।</p>
